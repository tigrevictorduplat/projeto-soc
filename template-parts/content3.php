    <h2>Depoimentos das Entrevistas Individuais</h2>

    <strong><p>Pergunta: Como foi o processo para entrar no mercado de trabalho? Foi um processo tranquilo?</p></strong>
    <em><p>"Eu entrei no mercado de trabalho aos 16 anos, iniciando como atendente de loja de roupas. Em uma questão racial, digo que inicialmente tive certa dificuldade ao me adaptar por sentir que tinha sido contratada por um processo “forçado” de inclusão visto que todas as outras funcionárias eram brancas e havia de mim, a única funcionária negra e de cabelo cacheado uma cobrança maior em relação a minha aparência do que das outras, principalmente em relação ao meu cabelo."</p>
    <p>"Também passei por outra experiência quando trabalhei por um pequeno período em uma loja delivery e outros funcionários tinha reações adversas por no período eu usar tranças no cabelo, geralmente comentários inconvenientes como dizer que eu tinha falta de higiene, por “não lavar o cabelo” por exemplo. <br>
    Atualmente, não tenho problemas em relação a isto, visto que no meu espaço atual a uma certa variedade de pessoas e não tenho o contato direto com o cliente."</p></em>
    <hr>
    <strong><p>Pergunta: Qual foi sua maior dificuldade como pessoa negra no mercado de trabalho?</p></strong>
    <em><p>Resposta: Acredito que algumas inseguranças internas, como por exemplo o pensamento: "Se eu estiver concorrendo com uma pessoa branca que possui exatamente as mesmas habilidades que eu, essa pessoa vai conseguir a vaga só por ser branca?" E dependendo da empresa e do ambiente (principalmente se tiver 99% das pessoas brancas) já bate a sensação que independente das minhas habilidades eu não vou conseguir essa vaga, ou de que esse lugar não é pra mim.</p></em>
