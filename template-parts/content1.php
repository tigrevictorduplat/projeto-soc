    <h2>Objetivo e Objeto de Estudo</h2>
    
    <p> <a href="https://www.ibge.gov.br/explica/desemprego.php">O censo de Desemprego <strong>do IBGE</strong></a> acusa <em>9,4 milhões de desempregados</em>, uma taxa de desemprego de 8,8%. Sendo o estado do Nordeste o líder na taxa de desemprego (representando um pouco mais que 12% dos desempregados do país), é factível imaginar que uma das causas desses números tão alarmantes seja a própria dificuldade de adentrar no mercado de trabalho. Na posição de estudantes do <strong>Instituto Federal da Bahia</strong>, Campus Camaçari, sob a orientação da professora Tereza Quadros, decidimos ir a fundo nessa tese e questionar nos questionar, através dessa pesquisa de campo, o quão díficil tem sido para nós estudantes adentrar no Mercado de Trabalho. 
    </p>

    <img src="../src/img/capas/desemprego.jpg" alt="Imagem Principal">

    <p>Não obstante à essa dificuldade, o Brasil é um país onde o racismo estrutural esta enraizado profundamente na nossa sociedade. O que pode ser facilmente atestado pelas constantes violência sociais, judiciais, estruturais que vivemos, como atestado na reportagem <a href="https://www.cartacapital.com.br/sociedade/seis-estatisticas-que-mostram-o-abismo-racial-no-brasil/">Seis estatísticas que mostram o abismo racial no Brasil</a><strong> da Carta Capital</strong>.
Cruzando essas duas realidades buscamos com nossa pesquisa explicitar, através dos dados obtidos, essa triste realidade que estamos vivendo.
</p>

