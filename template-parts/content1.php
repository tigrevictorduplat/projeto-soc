        <p>Em sua opinião, é difícil entrar no mercado de trabalho? Justifique</p>
    </strong>
    <ul><em>
        <li>“Sim, pois existem vários fatores que tornam essa ação um desafio. Podemos citar como alguns exemplos a dificuldade em conciliar estudo/trabalho, a grande taxa de desemprego no Brasil e também a falta de experiência profissional (que é um requisito muito comum em qualquer área).”</li>
        <li>“Sim, pela ampla concorrência entre os jovens e na maioria das vezes o calendário escolar que tem os seus horários bem bagunçados impossibilitando muitas vezes de poder trabalhar no turno oposto ao que estuda!”</li>
        <li>“Sim, independente da área. O mercado está inflado, poucas vagas pra muitos trabalhadores desempregados, isso se reflete mesmo em áreas de trabalho mais em alta (como por exemplo tecnologia)”</li>
        <li>“Por exemplo, em grandes casos atualmente é exigida experiência em um trabalho anterior, e para quem está a procura do seu primeiro emprego é mais difícil. Além de que existem ainda muitos preconceitos em relação ao jeito de se vestir, cabelo, cor e até mesmo gênero. Como por exemplo, existem vagas destinadas ao gênero masculino/feminino e sempre há uma diferença tanto na questão da oferta quanto no salário sugerido.”</li>
        <li>“Sim, principalmente na área de tecnologia, por ser mulher. As vagas são majoritariamente para homens, a prioridade.”</li>
    </ul></em>
