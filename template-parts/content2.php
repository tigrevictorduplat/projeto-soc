    <p>Além do que as Perguntas Gerais revelam, é apenas cruzando alguns dados em comum que podemos ter uma noção mais aprofundada de quem é a “Comunidade Acadêmica do Campus Camaçari”.</p>

   <h2>Perfil - Faixa Etária</h2>
    <img src="../src/img/perfil/perfil-idade.png" alt="Imagem Principal">
    <p>Fazendo uma média com as respostas obtidas à cerca da idade dos participantes, podemos verificar que tratam-se de jovens na faixa dos 16 à 23 anos, obtendo portanto uma média de 19 anos de idade.
</p>


<h2>Perfil - Gênero </h2>
    <img src="../src/img/perfil/perfil-genero-etnia.png" alt="Imagem Principal">
    <p>Quando relacionamos os dados de gênero e a etnia, autodeclarados, podemos perceber que uma grande parcela dos entrevistados se caracteriza como <em>Preto ou Pardo</em> sendo ao todo 19 e  apenas 7 autodeclarados Brancos. Destes 19, 8 declaram-se Pretos sendo 5 Homens e 3 Mulheres</p>
    
<h2>Perfil - Renda e Etnia</h2>
<img src="../src/img/perfil/perfil-renda-etnia.png" alt="Imagem Principal">
<p>Procurando relacionar o perfil socio-economico dos entrevistados com suas dificuldades e com a necessecidade de trabalhar, cruzamos os dados referentes à renda familiar deles e observou-se que:</p>
<strong><em>
<p> - 61% dos entrevistados tem renda familiar de menos de 2 salários minímos;</p>
<p> -  Desses 60%, 50% são Pretos ou Pardos; </p>
<p> - Dos 39% com mais de 2 salários minímos 60% são de etnia Branca. </p>
</em></strong>

